// Os operadores vão nos permitir fazer operações (humm é mesmo!? hahahaha...) matemáticas, de comparação e lógicas

// Aritméticos
console.log(2+2)
console.log(2-2)
console.log(2*2)
console.log(8/4)

// De comparação

// igual - Retorna verdadeiro se os valores comparados forem iguais.
console.log(2 == 2)
console.log(2 == 3)

// não igual - Retorna verdadeiro se os valores comparados não forem iguais. Esse operador também pode ser chamado de diferente de.
console.log(2 != 2)
console.log(2 != 1)

// igual estrito - Só retorna verdadeiro se o valor e o tipo comparados forem iguais.
console.log(2 === '2')

// Não igual estrito - Esse operador vai retornar verdadeiro se os valores e ou os tipos forem diferentes
console.log(3 !== '3')
console.log('3' !== '3')

// Maior que - Compara se o operador da esquerda é maior que o da direita. Caso seja retorna verdadeiro
console.log(2 > 3)
console.log(2 > 1)

// Menor que - Compara se o operador da esquerda é maior ou igual ao da direita. Caso seja retorna verdadeiro
console.log(2 < 3)
console.log(2 < 1)

// Maior igual - Compara se o operador da esquerda é menor que o da direita. Caso seja retorna verdadeiro

console.log(2 >= 3)
console.log(2 >= 2)
console.log(2 >= 1)

// Menor igual - Compara se o operador da esquerda é menor ou igual ao da direita. Caso seja retorna verdadeiro
console.log(2 <= 3)
console.log(2 <= 2)
console.log(2 <= 1)